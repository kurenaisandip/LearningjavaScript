// JS has 8 datatypes:

// 1. String 
// 2. Number 
// 3. Bigint 
// 4. Boolean 
// 5. Undefined 
// 6. Null 
// 7. Symbol 
// 8. Object 

// The Object Datatype 
// The object datatype can contain:
// 1. an object 
// 2. an Array
// 3. a date 


Examples
// Numbers:
let length = 16;
let weight = 7.5;

// Strings:
let color = "Yellow";
let lastName = "Johnson";

// Booleans
let x = true;
let y = false;

// Object:
const person = {firstName:"John", lastName:"Doe"};

// Array object:
const cars = ["Saab", "Volvo", "BMW"];

// Date object:
const date = new Date("2022-03-25");